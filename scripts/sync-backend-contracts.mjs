#!/usr/bin/env node
import { createHash } from 'node:crypto';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { execFileSync } from 'node:child_process';

const root = process.cwd();
const checkOnly = process.argv.includes('--check');
const lockPath = join(root, 'docs/backend-contract-lock.json');
const outputPath = join(root, 'lib/api/generated/contracts.ts');
const lock = JSON.parse(readFileSync(lockPath, 'utf8'));

const candidates = [
  process.env.MERAKI_BACKEND_REPO,
  '../meraki-protocol',
  '/home/tim/meraki-protocol-fresh-verify-20260718-0111',
  '/home/tim/meraki-repo-separation-20260718-0036/meraki-backend'
].filter(Boolean);

function sha256(value) {
  return createHash('sha256').update(value).digest('hex');
}

function findBackendRepo() {
  for (const candidate of candidates) {
    const repo = resolve(root, candidate);
    const schemaFile = join(repo, lock.schemaPath);
    if (existsSync(schemaFile)) return repo;
  }
  throw new Error(`Backend repository not found. Set MERAKI_BACKEND_REPO to a clone of ${lock.repository}@${lock.commit}.`);
}

function git(repo, args) {
  return execFileSync('git', ['-C', repo, ...args], { encoding: 'utf8' }).trim();
}

const backendRepo = findBackendRepo();
const backendHead = git(backendRepo, ['rev-parse', 'HEAD']);
if (backendHead !== lock.commit) {
  throw new Error(`Backend commit drift: expected ${lock.commit}, got ${backendHead} at ${backendRepo}`);
}

const schemaSource = readFileSync(join(backendRepo, lock.schemaPath), 'utf8');
const schemaHash = sha256(schemaSource);
if (schemaHash !== lock.schemaSha256) {
  throw new Error(`Backend schema hash drift: expected ${lock.schemaSha256}, got ${schemaHash}`);
}

const generated = `// Generated by ${lock.generator}. Do not edit by hand.
// Source: ${lock.repository}@${lock.commit}:${lock.schemaPath}
// Schema SHA-256: ${lock.schemaSha256}

import { z } from 'zod';

export const backendContractLock = ${JSON.stringify(lock, null, 2)} as const;

export const dataSourceSchema = z.object({
  chain_id: z.number(),
  source: z.enum(['api', 'snapshot', 'mock']),
  fetched_at: z.string().datetime()
});

export const postMigrationPoolSchema = z.object({
  schema: z.literal('PostMigrationPoolStateV1'),
  pool_address: z.string(),
  dex: z.enum(['uniswap_v3', 'aster_router_v1']),
  base_symbol: z.string(),
  quote_symbol: z.string(),
  price_native: z.string(),
  volume24h: z.string(),
  tvl_native: z.string()
});

export const tokenSummarySchema = z.object({
  schema: z.literal('TokenSummaryV1'),
  token_id: z.string(),
  token_address: z.string(),
  launchpad_market: z.string(),
  symbol: z.string().nullable(),
  name: z.string().nullable(),
  status: z.enum(['curve_live', 'migration_pending', 'migrated']),
  priceNative: z.string(),
  volume24h: z.string(),
  trades24h: z.number(),
  last_trade_time: z.string().nullable(),
  bonding_threshold: z.string(),
  bonding_progress: z.number(),
  migration_status: z.enum(['not_ready', 'ready', 'migrated']),
  migration_pool: postMigrationPoolSchema.optional(),
  __source: dataSourceSchema.optional()
});

export const recentTradeSchema = z.object({
  schema: z.literal('RecentTradeV1'),
  trade_id: z.string(),
  token_id: z.string(),
  token_address: z.string(),
  token_name: z.string().nullable(),
  direction: z.enum(['buy', 'sell']),
  stage: z.enum(['bonding_curve', 'post_migration']),
  quote_token_address: z.string().nullable(),
  quote_symbol: z.string(),
  quote_amount: z.string(),
  base_amount: z.string().nullable(),
  execution_price_native: z.string().nullable(),
  tx_hash: z.string(),
  timestamp: z.string()
});

export const discoverySummarySchema = z.object({
  schema: z.literal('DiscoverySummaryV1'),
  generatedAt: z.string().datetime(),
  latestTokens: z.array(tokenSummarySchema),
  mostActiveTokens: z.array(tokenSummarySchema),
  recentTrades: z.array(recentTradeSchema),
  stats: z.object({
    hotCount: z.number(),
    movingCount: z.number(),
    migrationReadyCount: z.number()
  }),
  __source: dataSourceSchema.optional()
});

export const apiErrorSchema = z.object({
  schema: z.literal('ApiErrorV1'),
  error_code: z.string(),
  message: z.string(),
  correlation_id: z.string()
});

export const tokenTradesResponseSchema = z.object({
  token_id: z.string(),
  trades: z.array(recentTradeSchema),
  __source: dataSourceSchema.optional()
});

export type BackendDataSourceV1 = z.infer<typeof dataSourceSchema>;
export type PostMigrationPoolStateV1 = z.infer<typeof postMigrationPoolSchema>;
export type TokenSummaryV1 = z.infer<typeof tokenSummarySchema>;
export type RecentTradeV1 = z.infer<typeof recentTradeSchema>;
export type DiscoverySummaryV1 = z.infer<typeof discoverySummarySchema>;
export type ApiErrorV1 = z.infer<typeof apiErrorSchema>;
export type TokenTradesResponseV1 = z.infer<typeof tokenTradesResponseSchema>;
`;

if (checkOnly) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';
  if (current !== generated) {
    throw new Error(`${outputPath} is out of sync with ${lock.repository}@${lock.commit}`);
  }
  console.log(`Backend contract drift check passed for ${lock.repository}@${lock.commit}`);
} else {
  writeFileSync(outputPath, generated);
  console.log(`Wrote ${outputPath} from ${lock.repository}@${lock.commit}`);
}
